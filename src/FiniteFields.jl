abstract FiniteFieldElem <: FieldElem

include("fq.jl")

include("fq_nmod.jl")

###########################################################################################
#
#   GCD
#
###########################################################################################

function gcd{T <: FiniteFieldElem}(x::T, y::T)
   check_parent(x, y)
   return iszero(x) && iszero(y) ? zero(parent(y)) : one(parent(y))
end
